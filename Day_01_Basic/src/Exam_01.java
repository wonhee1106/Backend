// 2. 클래스 이름 첫글자는 반드시 대문자 / 범위 - Global
 
public class Exam_01 // 파일명과 똑같이
{
	public static void main(String[] args) {//초기 실행위치
		System.out.print("Hello World \t"); // 컴퓨터야 모니터에 텍스트를 출력해줘. 사용 빈도 아주 높음
		System.out.println(1234);
		System.out.println(3.14);
		System.out.println('A');
				
		// 데이터의 유형을 구분짓기 위해 사용하는 기호
		// 문자열을 다룰 때에는 Double Quote (쌍따옴표) 를 사용한다.
		// 단일문자를 다룰 때에는 Single Quote (홑따옴표) 를 사용한다.
		// 숫자 데이터를 다룰 때에는 Quote를 사용하지 않는다.
	}
}
